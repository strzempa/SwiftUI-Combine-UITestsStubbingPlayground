import Foundation

enum AccessibilityIdentifiers {
    enum Alert {
        struct Buttons {
            static let close = "Close"
        }
    }
    enum List {
        struct Labels {
            static let header = "Main.Labels.Header"
        }
        struct Buttons {
            static let fetch = "Main.Buttons.Fetch"
        }
        struct Items {
            static let item = "Main.Buttons.Item"
        }
    }
}
